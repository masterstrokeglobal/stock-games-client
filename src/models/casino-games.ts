export const GameCategories = Object.values(GameCategory).map((category) => ({
    label: category.replace(/_/g, " ").charAt(0).toUpperCase() + category.replace(/_/g, " ").slice(1),
    value: category
}))

export enum GameStatus {
    ACTIVE = "active",
    IN_ACTIVE = "in_active",
}


class CasinoGames {
    id!: number;
    name!: string;
    imageUrl!: string;
    providerName!: ProviderEnum;
    subProviderName!: ProviderEnum;
    category!: GameCategory;
    status!: GameStatus;
    popular!: boolean;
    new!: boolean;  
    code!: string;
    gameId!: number;
    createdAt!: Date;
    updatedAt!: Date;
    deletedAt?: Date;

    constructor(params: CasinoGames) {
        this.id = params.id;
        this.name = params.name;
        this.imageUrl = params.imageUrl;
        this.providerName = params.providerName;
        this.subProviderName = params.subProviderName;
        this.category = params.category;
        this.status = params.status;
        this.code = params.code;
        this.gameId = params.gameId;
        this.popular = params.popular;
        this.new = params.new;
        this.createdAt = params.createdAt;
        this.updatedAt = params.updatedAt;
        this.deletedAt = params.deletedAt;
    }

    get statusLabel() {
        return this.status === GameStatus.ACTIVE ? "Active" : "Inactive";
    }
}


export default CasinoGames;
